C = A - b;
console.log( "Resultado de resta es:" + C );
//Multiplicacion
C = A * b;
console.log( "Resultado de multiplicacion es:" + C );
//Division
C = A / b;
console.log( "Resultado de division es:" + C );
//Expresion de exponente
C = A ** b;
console.log( "Resultado de el exponente es:" + C );
//Operador de modulo
C = A % b;
console.log( "Resultado de modulo es:" + C );
